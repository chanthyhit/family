// Function to create a tree node
function createTreeNode(nodeData) {
    const nodeElement = document.createElement('li');
    nodeElement.classList.add('tree');

    // Extract the sex value
    // const sex = nodeData.sex === "ážŸ" ? 'ðŸ‘©' : 'ðŸ‘±';

    // Set the style attribute to list-style with sex value
    // nodeElement.setAttribute('style', `list-style: '${sex} ';`);

    const img = document.createElement('img');
    img.src = `images/${nodeData.name}.jpg`;

    //console.log(`images/${nodeData.name}.jpg`)

    // Set the onerror event to handle image loading errors
    img.onerror = function() {
        const avatar = nodeData.sex === "ážŸ" ? 'female.png' : 'male.png';
        img.src = `images/${avatar}`;
    };
    nodeElement.appendChild(img);

    // Check if the node has children
    if (nodeData.children) {
        // Create the details and summary elements
        const details = document.createElement('details');
        details.setAttribute('open', '');

        const summary = document.createElement('summary');
        summary.textContent = `${nodeData.name}`;
        details.appendChild(summary);

        const childrenContainer = document.createElement('ul');

        // Recursively create child nodes
        nodeData.children.forEach(child => {
            const childNode = createTreeNode(child);
            childrenContainer.appendChild(childNode);
        });

        details.appendChild(childrenContainer);
        nodeElement.appendChild(details);
    } else {
        const span = document.createElement('span');
        span.textContent = `${nodeData.name}`;
        nodeElement.appendChild(span);
    }

    return nodeElement;
}

// Function to render the tree
function renderTree(treeData, containerId) {
    const treeContainer = document.getElementById(containerId);

    treeData.forEach(rootNodeData => {
        const rootNode = createTreeNode(rootNodeData);
        treeContainer.appendChild(rootNode);
    });
}

// Render the tree
renderTree(treeData, 'tree-container');



